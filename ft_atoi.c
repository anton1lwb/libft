int ft_atoi(char *str)
{
    int nb;
    int sign;

    sign = 1;
    nb = 0;
    if (*str && (*str == '-' || *str == '+'))
    {
        if (*str == '-')
            sign = -1;
        str++;
    }   
    while (*str && (*str >= '0' || *str <= '9'))
    {
        nb = nb * 10 + (*str - 48);
        str++;
    }
    return (nb * sign);
}