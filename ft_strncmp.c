int ft_strncmp(char *s1, char *s2, int size)
{
    int i;

    i = 0;
    if (!s1[i] || !s2[i])
	    return (-1);
    while(s1[i] && i <= size)
    {
	if (s1[i] != s2[i])	
	    return(s1[i] - s2[i]);
	i++;
    }
    return (0);
}
